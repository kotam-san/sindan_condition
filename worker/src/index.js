export default {
  async fetch(request, env) {
    if (request.method === "OPTIONS") {
      return new Response(null, { headers: CORS });
    }

    if (request.method !== "POST") {
      return new Response("Method Not Allowed", { status: 405, headers: CORS });
    }

    try {
      const { email, stage, topAxis, axisScores, total, freeText } = await request.json();

      const freeTextLine = freeText
        ? `\n今、一番気になっていること：「${freeText}」\n`
        : "";

      const prompt = `あなたは後継者・若手経営者を専門とするエグゼクティブコーチです。以下のコンディション診断の結果をもとに、プロのコーチとして深い洞察と具体的な提案を書いてください。

【診断スコア（各20点満点）】
・${AXIS_NAMES.A}：${axisScores.A}点
・${AXIS_NAMES.B}：${axisScores.B}点
・${AXIS_NAMES.C}：${axisScores.C}点
・${AXIS_NAMES.D}：${axisScores.D}点
・総合スコア：${total}点（80点満点）
・最も高い軸：${AXIS_NAMES[topAxis]}
・ステージ：${stage}「${STAGE_NAMES[stage]}」
${freeTextLine}
【出力ルール】
以下の6つのセクションを「|||」のみで区切って出力する。見出し・番号・記号は含めない。

セクション1：客観的分析（200〜250字）
今この方に起きていることを、コーチの目線で客観的かつ共感を持って分析する。スコアから見える構造的な課題を、「これは弱さではなく〜」という視点で伝える。

セクション2：コーチからの問い（80〜120字）
現状のパターンをリセットするための、一つの問いかけ。「もし〜〜だとしたら」形式を使う。

セクション3：アクション1（「タイトル（15字以内）：具体的な説明（60〜80字）」の形式）
明日からできる最小の一歩。

セクション4：アクション2（「タイトル（15字以内）：具体的な説明（60〜80字）」の形式）

セクション5：アクション3（「タイトル（15字以内）：具体的な説明（60〜80字）」の形式）

セクション6：専門家からのメッセージ（100〜150字）
温かく、的確に次の一歩を促すメッセージ。「まずは〜〜」で締めくくる。

口調は「です・ます」で温かく。上から目線にならず、同じ目線で語りかける。経営者の孤独や疲弊に共感したうえで、次の一歩を示す。`;

      let sections = [];

      if (env.ANTHROPIC_API_KEY) {
        const claudeRes = await fetch("https://api.anthropic.com/v1/messages", {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            "x-api-key": env.ANTHROPIC_API_KEY,
            "anthropic-version": "2023-06-01",
          },
          body: JSON.stringify({
            model: "claude-haiku-4-5-20251001",
            max_tokens: 1500,
            messages: [{ role: "user", content: prompt }],
          }),
        });

        if (claudeRes.ok) {
          const claudeData = await claudeRes.json();
          const text = claudeData.content[0].text;
          sections = text.split("|||").map((s) => s.trim()).filter(Boolean).slice(0, 6);
        }
      }

      if (sections.length < 6) {
        sections = FALLBACK[topAxis] || FALLBACK.D;
      }

      return new Response(JSON.stringify({ success: true, suggestions: sections }), {
        headers: { "Content-Type": "application/json", ...CORS },
      });
    } catch (error) {
      return new Response(JSON.stringify({ error: error.message }), {
        status: 500,
        headers: { "Content-Type": "application/json", ...CORS },
      });
    }
  },
};
